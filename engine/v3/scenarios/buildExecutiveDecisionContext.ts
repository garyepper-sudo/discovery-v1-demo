import type { OrganizationRuntime } from "../runtime/organizationRuntime";
import type {
  RunExecutiveScenarioInput,
} from "./runExecutiveScenario";

export type ExecutiveDecisionContext =
  Omit<RunExecutiveScenarioInput, "intervention">;

type ScenarioRuntimeMemory =
  OrganizationRuntime["memory"] & {
    executiveAssessment?:
      RunExecutiveScenarioInput["currentExecutiveAssessment"];

    organizationalState?:
      RunExecutiveScenarioInput["currentOrganizationalState"];

    organizationalConditions?:
      RunExecutiveScenarioInput["simulation"]["conditions"];

    organizationalPredictions?:
      RunExecutiveScenarioInput["simulation"]["predictions"];

    predictionEvaluations?:
      RunExecutiveScenarioInput["simulation"]["predictionEvaluations"];

    organizationalLearningProfile?:
      RunExecutiveScenarioInput["simulation"]["learningProfile"];

    organizationalCausalModel?:
      RunExecutiveScenarioInput["simulation"]["causalModel"];

    organizationalJudgments?:
      RunExecutiveScenarioInput["judgments"];

    mechanismNetwork?: {
      mechanisms?:
        RunExecutiveScenarioInput["mechanisms"];
    };

    conceptCandidates?:
      RunExecutiveScenarioInput["conceptCandidates"];

    conceptualUnderstanding?:
      RunExecutiveScenarioInput["conceptualUnderstanding"];

    investigationOpportunities?:
      RunExecutiveScenarioInput["investigationOpportunities"];
  };

function requireValue<T>(
  value: T | null | undefined,
  label: string,
): T {
  if (value === null || value === undefined) {
    throw new Error(
      `Executive decision context is unavailable: ${label} is missing. Run at least one investigation before evaluating scenarios.`,
    );
  }

  return value;
}

function asArray<T>(
  value: T[] | null | undefined,
): T[] {
  return Array.isArray(value) ? value : [];
}

/**
 * Adapts the persisted Organization Runtime into the canonical input expected
 * by runExecutiveScenario().
 *
 * This function performs no organizational reasoning and does not mutate the
 * runtime. It exists solely to keep API and UI layers independent of runtime
 * storage details.
 */
export function buildExecutiveDecisionContext(
  runtime: OrganizationRuntime,
  params: {
    changedEntityId: string;
    interventionDelta: number;
    timeHorizon?:
      RunExecutiveScenarioInput["simulation"]["timeHorizon"];
    simulatedAt?: string;
  },
): ExecutiveDecisionContext {
  const memory =
    runtime.memory as ScenarioRuntimeMemory;

  /**
   * Use the canonical organizational beliefs generated by the current
   * organizational-understanding pipeline.
   *
   * organizationalMemory.beliefs contains legacy PersistentBelief objects
   * and is not compatible with the OrganizationalBelief objects required by
   * simulation and executive assessment.
   */
  const organizationalBeliefs =
    memory.organizationalUnderstandingState
      ?.organizationalBeliefs ??
    [];

  const mechanisms =
    memory.mechanismNetwork?.mechanisms ??
    [];

  const theories =
    memory.organizationalMemory?.theories ??
    memory.theories ??
    [];

  /**
   * Simulation requires the complete OrganizationalLearningProfile.
   *
   * OrganizationalMemoryMaturity is only a maturity summary and must not be
   * used as a fallback for the full learning profile.
   */
  const learningProfile =
    memory.organizationalLearningProfile;

  return {
    simulation: {
      organizationId:
        runtime.metadata.organizationId,

      causalModel:
        requireValue(
          memory.organizationalCausalModel,
          "organizationalCausalModel",
        ),

      changedEntityId:
        params.changedEntityId,

      interventionDelta:
        params.interventionDelta,

      conditions:
        asArray(
          memory.organizationalConditions,
        ),

      beliefs:
        asArray(organizationalBeliefs),

      predictions:
        asArray(
          memory.organizationalPredictions,
        ),

      predictionEvaluations:
        asArray(
          memory.predictionEvaluations,
        ),

      learningProfile:
        requireValue(
          learningProfile,
          "organizationalLearningProfile",
        ),

      simulatedAt:
        params.simulatedAt ??
        new Date().toISOString(),

      timeHorizon:
        params.timeHorizon ??
        "near-term",
    },

    currentExecutiveAssessment:
      requireValue(
        memory.executiveAssessment,
        "executiveAssessment",
      ),

    currentOrganizationalState:
      requireValue(
        memory.organizationalState,
        "organizationalState",
      ),

    judgments:
      asArray(
        memory.organizationalJudgments,
      ),

    mechanisms:
      asArray(mechanisms),

    conceptCandidates:
      asArray(
        memory.conceptCandidates,
      ),

    conceptualUnderstanding:
      asArray(
        memory.conceptualUnderstanding,
      ),

    organizationalBeliefs:
      asArray(organizationalBeliefs),

    investigationOpportunities:
      asArray(
        memory.investigationOpportunities,
      ),

    theories:
      asArray(theories),
  };
}